package ImportantProgram;

public class Print500to1Decending {
	
	public static void main(String[] args) {
		
		
		for(int i=0; i<=500; i++) {
			System.out.println(i +"velocity");
		}
		
		System.out.println("=================");
		
		for(int i=500; i>=1; i--) {
			System.out.println(i +"velocity");
		}
	}

}
