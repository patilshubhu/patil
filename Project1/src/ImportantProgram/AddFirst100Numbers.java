package ImportantProgram;

public class AddFirst100Numbers {
	
	public static void main(String[] args) {
	                          
	    //sum of n natural numbers=n*(n+1)/2 
	
	     //sum=100*100+1%2=100*50.5=5050
	
		int num = 100;
		int sum=0;
		
		for(int i=1; i<=num; i++) {
			sum=sum+i;
		}
		System.out.println(sum);
			
		}
		
		
		
		}

		
		
		
		
		
	


