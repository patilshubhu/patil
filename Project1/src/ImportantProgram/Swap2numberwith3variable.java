package ImportantProgram;

public class Swap2numberwith3variable {
	
	public static void main(String[] args) {
		
		int a=100;
		int b=200;
		int t; t=a; a=b; b=t;     // t=a        t=100
		System.out.println(a);    // 100=200    a=b
		System.out.println(b);     //200=t      b=t
	}

}
                                       