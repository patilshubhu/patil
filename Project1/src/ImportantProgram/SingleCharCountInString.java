package ImportantProgram;

public class SingleCharCountInString {
	public static void main(String[] args) {
		
		String s="roopesh";
		int count =0;
		for(int i=0; i<s.length(); i++) {
		   if(s.charAt(i)=='o')	{
			   count=count+1;
		   }
		   }
		System.out.println("count the number of o in a string= "+count);
		}
		
		
		
		
		
		
		
		
		
		
		
		
	}


