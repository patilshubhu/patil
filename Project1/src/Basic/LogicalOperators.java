package Basic;

public class LogicalOperators {
	public static void main(String[] args) {
	int x=70;
	int y=30;
	//Arithmetic operators
	System.out.println("======Arithmetic operators");
	System.out.println("sum of x and y is:"+(x+y));
	System.out.println("diff of x and y is:"+(y-x));
	System.out.println("mul of x and y is:"+(x*y));
	System.out.println("div of x and y is:"+(x/y));
	System.out.println("mod div of x and y is:"+(x%y));
	
	//Relational operator(comparison operator)
	System.out.println("Relational operator");
	System.out.println(x==y);
	System.out.println(x>y);
	System.out.println(x<y);
	System.out.println(x>=y);
	System.out.println(x<=y);
	System.out.println(x!=y);
	
	//Logical operator
	boolean a=true;
	boolean b=false;
	System.out.println("====Logical operator===");
	System.out.println(a&&b);
	System.out.println(a||b);
	System.out.println(!a);
	System.out.println(!b);
	
	//Increment and decrement
	System.out.println("===Increment and decrement===");
	x++;
	System.out.println(x++);
	y--;
	
	System.out.println(y--);
	
	
	
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
